package org.onehippo.forge.exdocpicker.demo.jpa.model;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "INFORMANT") 
public class Informant implements Serializable {

    private static final long serialVersionUID = 1L;

    @Id
    @Column(name = "ID")
    private Long id;

    @Column(name = "NAME")
    private String name;

    @Column(name = "DESCRIPTION")
    private String description;

    @Column(name = "PHOTO")
    private String photo;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    @Override
    public boolean equals(Object other) {
        if (!(other instanceof Informant)) {
            return false;
        }

        if ((id == null && ((Informant) other).id == null) || (id.equals(((Informant) other).id))) {
            return true;
        }

        return false;
    }

    @Override
    public int hashCode() {
        if (id == null) {
            return -1;
        }

        return id.hashCode();
    }

    @Override
    public String toString() {
        return "Informant(id=" + id + ",name=" + name + ")";
    }
}
